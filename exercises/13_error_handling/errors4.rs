#[derive(PartialEq, Debug)]
enum CreationError {
    Negative,
    Zero,
}

#[derive(PartialEq, Debug)]
struct PositiveNonzeroInteger(u64);

impl PositiveNonzeroInteger {
    fn new(value: i64) -> Result<Self, CreationError> {
        // TODO: 此函数不应总是返回 `Ok`。
        // 阅读下面的测试，确认应该返回什么。
        Ok(Self(value as u64))
    }
}

